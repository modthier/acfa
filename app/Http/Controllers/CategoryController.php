    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function show($slug)
    {
        $category = Category::whereTranslation('slug', $slug)->firstOrFail();
        $lang = app()->getLocale();
        if ($category->translate()->where('slug', $slug)->first()->locale != $lang) {
            return redirect()->route('main.category.show', $category->translate()->slug);
        }
        $courses = $category->courses()->orderBy('id','desc')->paginate(12);
        return view('main.category.show')
            ->with([
                'category' => $category ,
                'courses' => $courses
            ]);
    }

